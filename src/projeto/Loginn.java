    private void removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerActionPerformed
        // TODO add your handling code here:
        for(Local c: pessoa.getLocais()){
            if(((String)seus_locais.getSelectedItem()).equals(c.getNome())){
                pessoa.remove_local(c);
                CleanCombo();
                FillCombo_seus_locais();
                erro.setText(((String)seus_locais.getSelectedItem())+" Removido com sucesso");
                break;
            }
        }
    }//GEN-LAST:event_removerActionPerformed

