    private void adicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarActionPerformed
        // TODO add your handling code here:
        for(Local c: dei.getLocais_conv()){
            if(((String)lista_locais.getSelectedItem()).equals(c.getNome())){
                int a=pessoa.add_local(c);
                switch(a){
                    case -2:
                        erro.setText("Ja esta inscrito no local");
                        break;
                    case -3:
                        erro.setText("Nao se pode inscrever em mais de 5 locais");
                        break;
                    case 0:
                        erro.setText(((String)lista_locais.getSelectedItem())+" Adicionado com sucesso");
                        CleanCombo();
                        FillCombo_seus_locais();
                        break;
                    case -1:
                        erro.setText(((String)lista_locais.getSelectedItem())+" tem lotacao esgotada");
                        break;
                }
            }           
        }
    }//GEN-LAST:event_adicionarActionPerformed

