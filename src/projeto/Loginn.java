    /** Método para o preenchimento da combobox lista_locais com os locais do convivio
     */
    public void FillCombo_lista_locais(){       
        for(Local c: dei.getLocais_conv()){
            lista_locais.addItem(c.getNome());
        }
    }
    
    /** Método que limpa a combobox seus_locais
     */
    public void CleanCombo(){
        for(Local c: dei.getLocais_conv()){
            seus_locais.removeItem(c.getNome());
        }
    }
    
    /** Método para o preenchimento da combobox sues_locais com os locais do convivio em que a pessoa esta inscrita
     */
    public void FillCombo_seus_locais(){
        for(Local c: pessoa.getLocais()){
            seus_locais.addItem(c.getNome());
        }
    }
    
